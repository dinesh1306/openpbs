/**
 * @brief
 *      Checkpoint the job. 
 *
 * @param[in] ptask - pointer to task
 * @param[in] file - filename
 * @param[in] abort - value indicating abort
 *              
 * If abort is true, kill it too.
 *      
 * @return      int
 * @retval      -1      error
 * @retval      0       Success
 */
int
mach_checkpoint(task *ptask, char *file, int abort)
{
#if MOM_CHECKPOINT == 1
	struct	stat	sbuf;
	long		options = 0;
	id_t    	jid = ptask->ti_qs.ti_u.ti_ext.ti_jid;
	pid_t		sid = ptask->ti_qs.ti_sid;

	/* if no sid is set, no point in going on */
	if (sid <= 1) {
		sprintf(log_buffer, "No sid for task %8.8X",
			ptask->ti_qs.ti_task);
		log_err(-1, __func__, log_buffer);
		return 0;
	}	/* no need to checkpoint if file exists and sid is gone */
	else if (kill(sid, 0) != 0 && errno == ESRCH &&
		stat(file, &sbuf) == 0) {
		sprintf(log_buffer, "task %8.8X sid=%d does not exist and "
			"checkpoint file %s exists",
			ptask->ti_qs.ti_task, sid, file);
		log_err(-1, __func__, log_buffer);
		return 0;
	}

	if (abort)
		options |= CHKPNT_KILL;
	if (jid > 0)
		return chkpnt(CHKPNT_JOB, jid, file, options);
	else
		return chkpnt(CHKPNT_FAMILY, sid, file, options);
#else /* MOM_CHECKPOINT */
	return (-1);
#endif /* MOM_CHECKPOINT */
}

