/**
 * @brief
 *	Get the info required for tm_attach.
 *
 * @param[in] pid - process id
 * @param[in] sid - session id
 * @param[in] uid - user id
 * @param[in] comm - command name
 * @param[in] len - size of command
 *
 * @return      int
 * @retval      TM_OKAY                 Success
 * @retval      TM_ENOPROC(17011)       Error
 *
 */
int
dep_procinfo(pid_t pid, pid_t *sid, uid_t *uid, char *comm, size_t len)
{
	int		i;
	proc_t		*pp;

	if (getprocs() == 0)
		return TM_ESYSTEM;

	for (i=0; i<nproc; i++) {
		pp = &proct[i];

		if (BOGUS_PROC(pp))
			continue;

		if (pp->p_pid == pid) {
			*sid = pp->p_sid;
			*uid = pp->p_uid;
			memset(comm, '\0', len);
			memcpy(comm, pp->p_ucomm,
				MIN(len-1, sizeof(pp->p_ucomm)));
			return TM_OKAY;
		}
	}
	return TM_ENOPROC;
}

