/*
 ** read nbytes from kmem at addr into to
 */
int
getkmem(long addr, void *to, long nbytes, char *what)
{
	static	char	id[] = "getkmem";

	if (lseek(kmemfd, (llong)addr, 0) == -1) {
		sprintf(log_buffer, "lseek: %s", what);
		log_err(errno, id, log_buffer);
		return 1;
	}
	if (nbytes != read(kmemfd, to, nbytes)) {
		sprintf(log_buffer, "read: %s", what);
		log_err(errno, id, log_buffer);
		return 1;
	}
	return 0;
}

