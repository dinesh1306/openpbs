/* cpupercent rises: how far towards the new estimated cpupercent should
 the job cpupercent be moved.

 0.0- cpupercent doesn't move up
 1.0- for samples at least MAX_CHECK_POLL_TIME apart, the new estimate
 becomes cpupercent

 Values in between make cpupercent a moving average - the higher, the faster
 job cpupercent tracks measurements
 */
extern double delta_weightup;

/* cpupercent falls: how far towards the new estimated cpupercent should
 the job cpupercent be moved.

 0.0- cpupercent doesn't move down - use this if you want cpupercent
 to correctly track the maximum "instantaneous" usage up to "now"
 1.0- for samples at least MAX_CHECK_POLL_TIME apart, the new estimate
 becomes cpupercent

 Values in between make cpupercent a moving average.

 Note that cpupercent is *never* adjusted down to under the cpupercent
 averaged for the total of the run so far or ncpus*100 if it has
 exceeded either of these, and never adjusted down at all
 if it's under either of these two values -
 - so it retains its semantics as a measure of the max. instantaneous
 usage in "sane" circumstances.

 Values in between 0 and 1 make cpupercent a moving average -
 the higher the value, the more rapidly it drops
 */
extern double delta_weightdown;

